
public class Numbers {
	private  int a;
	private  int b;
	private  int sum;
	public Numbers (){
		this.setA(a);
		this.setB(b);
		this.sum =getA()+getB();
	}
	
	
	public  int getSum() {
		return sum;
	}


	public  int getA() {
		return a;
	}
	public  void setA(int a) {
		this.a = a;
	}
	public  int getB() {
		return b;
	}
	public  void setB(int b) {
		this.b = b;
	}

}
